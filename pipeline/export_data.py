def load_ingest_runs(con) -> list[dict]:
    """Full-map ingestion runs (pipeline/ingest_map.py) for the Vision Lab
    — calibration health, team identity, CV ban detections, round/swap
    counts, all linked to that run's evidence report. This is the newer,
    richer sibling of load_auto_runs (which only covers the older capture/
    detect batch path) and is what runs.html leads with."""
    try:
        rows = con.execute(
            "SELECT * FROM ingest_runs ORDER BY created_at DESC").fetchall()
    except Exception:      # table missing on a very old DB -> empty, not fatal
        return []
    out = []
    for r in rows:
        ingest_id = r["id"]
        match = con.execute("SELECT * FROM matches WHERE id=?",
                            (r["match_id"],)).fetchone()
        mr = con.execute(
            "SELECT * FROM map_results WHERE match_id=? AND map_order=?",
            (r["match_id"], r["map_order"])).fetchone()
        map_result_id = mr["id"] if mr else None
        n_rounds = (con.execute(
            "SELECT COUNT(*) FROM map_rounds WHERE map_result_id=?",
            (map_result_id,)).fetchone()[0] if map_result_id else 0)
        n_stints = con.execute(
            "SELECT COUNT(*) FROM hero_stints WHERE ingest_id=?",
            (ingest_id,)).fetchone()[0]
        n_swaps_confirmed = con.execute(
            "SELECT COUNT(*) FROM hero_swaps WHERE ingest_id=? "
            "AND status='confirmed'", (ingest_id,)).fetchone()[0]
        n_swaps_uncertain = con.execute(
            "SELECT COUNT(*) FROM hero_swaps WHERE ingest_id=? "
            "AND status='uncertain'", (ingest_id,)).fetchone()[0]
        n_bans = con.execute(
            "SELECT COUNT(*) FROM hero_bans WHERE ingest_id=?",
            (ingest_id,)).fetchone()[0]
        n_ban_candidates = con.execute(
            "SELECT COUNT(*) FROM ingest_findings WHERE ingest_id=? "
            "AND kind='ban_candidate'", (ingest_id,)).fetchone()[0]
        team_findings = [{
            "side": rv(tf, "field"), "team": rv(tf, "value"),
            "confidence": rv(tf, "confidence"), "status": rv(tf, "status"),
            "notes": rv(tf, "notes"),
        } for tf in con.execute(
            "SELECT * FROM ingest_findings WHERE ingest_id=? "
            "AND kind='team_identity' ORDER BY field", (ingest_id,))]
        try:
            calib_health = (json.loads(r["calibration_health"])
                            if rv(r, "calibration_health") else None)
        except (ValueError, TypeError):
            calib_health = None
        out.append({
            "id": ingest_id,
            "sourceId": rv(r, "source_id"),
            "matchId": r["match_id"],
            "matchTitle": (f"{match['team_a']} vs {match['team_b']}"
                          if match else r["match_id"]),
            "mapOrder": r["map_order"],
            "mapId": rv(mr, "map_id") if mr else None,
            "startOffset": rv(r, "start_offset"),
            "endOffset": rv(r, "end_offset"),
            "detectorVersion": r["detector_version"],
            "calibrationVersion": rv(r, "calibration_version"),
            "status": r["status"],
            "calibrationStatus": rv(r, "calibration_status", "ok"),
            "calibrationHealth": calib_health,
            "reportPath": rv(r, "report_path"),
            "createdAt": rv(r, "created_at"),
            "rounds": n_rounds,
            "stints": n_stints,
            "swapsConfirmed": n_swaps_confirmed,
            "swapsUncertain": n_swaps_uncertain,
            "bansConfirmed": n_bans,
            "banCandidates": n_ban_candidates,
            "teamFindings": team_findings,
        })
    return out


def build_payload(con) -> dict:
    heroes = attach_portraits([dict(r) for r in con.execute(
        "SELECT id, name, role FROM heroes ORDER BY role, name")])
    maps = [dict(r) for r in con.execute(
        "SELECT id, name, mode FROM game_maps ORDER BY name")]
    teams = [{
        "id": r["id"],
        "name": r["name"],
        "region": r["region"],
        "code": r["code"],
        "faceitTeamId": rv(r, "faceit_team_id"),
        "logoUrl": rv(r, "logo_url"),
        "prepNotes": rv(r, "prep_notes"),
    } for r in con.execute(
        "SELECT * FROM teams ORDER BY name")]

    prep_notes = [{
        "id": r["id"],
        "teamId": r["team_id"],
        "opponentTeamId": rv(r, "opponent_team_id"),
        "map": rv(r, "map_id"),
        "noteType": rv(r, "note_type", "general"),
        "note": r["note"],
        "source": rv(r, "source", "manual"),
        "updatedAt": rv(r, "updated_at"),
    } for r in con.execute(
        "SELECT * FROM team_prep_notes ORDER BY team_id, note_type, id")]

    matches_out = []
    for m in con.execute(
        """SELECT * FROM matches
           WHERE status IN ('upcoming','live','final','unknown')
           ORDER BY date, id"""
    ).fetchall():
        maps_out = []
        replay_codes: list[str] = []
        for mr in con.execute(
            "SELECT * FROM map_results WHERE match_id=? ORDER BY map_order",
            (m["id"],),
        ).fetchall():
            info_a = comp_info_for_map(con, mr["id"], m["team_a"])
            info_b = comp_info_for_map(con, mr["id"], m["team_b"])
            bans = hero_bans_for_map(con, m["id"], mr["id"])
            replay_code = rv(mr, "replay_code")
            if replay_code:
                replay_codes.append(replay_code)
            side = winner_side(m, rv(mr, "winner_team"))
            confidence_values = [v for v in [info_a["confidence"], info_b["confidence"], rv(mr, "confidence")] if v is not None]
            confidence = round(sum(confidence_values) / len(confidence_values), 3) if confidence_values else None
            tl_a = comp_timeline(con, mr["id"], m["team_a"])
            tl_b = comp_timeline(con, mr["id"], m["team_b"])
            detected = bool(info_a["comp"] or info_b["comp"])
            maps_out.append({
                "id": mr["id"],
                "map": mr["map_id"],
                "winner": side,                      # derived convenience
                # -------- FACEIT-sourced (matchroom facts) ----------------
                "faceit": {
                    "mapOrder": mr["map_order"],
                    "mapScores": {"a": rv(mr, "score_a"), "b": rv(mr, "score_b")},
                    "winnerTeam": rv(mr, "winner_team"),
                    "pickedByTeam": rv(mr, "picked_by_team"),
                    "vetoAction": rv(mr, "veto_action"),
                    "pickVeto": rv(mr, "pick_veto"),
                    "heroBans": bans,
                    "replayCode": replay_code,
                    "replayCodes": [replay_code] if replay_code else [],
                    "replayExpiresNote": rv(mr, "replay_expires_note"),
                    "vodUrl": rv(mr, "vod_url"),
                    "vodStartSeconds": rv(mr, "vod_start_seconds"),
                    "source": rv(mr, "source", "unknown"),
                    # provenance of the fact fields (replay/bans): 'manual_facts'
                    # if any manual fact bans exist or the map is manual-sourced,
                    # else the map's own source ('faceit' / 'sample').
                    "factSource": ("manual_facts"
                                   if any(b.get("source") == "manual_facts" for b in bans)
                                   else rv(mr, "source", "unknown")),
                },
                # -------- Tracker-generated (replay/VOD/CV/manual) --------
                "tracker": {
                    "detected": detected,
                    "openerCompA": info_a["opener"],
                    "openerCompB": info_b["opener"],
                    "playedHeroesA": info_a["comp"],
                    "playedHeroesB": info_b["comp"],
                    "swapsA": info_a["swaps"],
                    "swapsB": info_b["swaps"],
                    "compTimeline": {"a": tl_a, "b": tl_b},
                    "confidence": confidence if detected else None,
                    "source": ("manual" if any(x["source"] == "manual" for x in (tl_a + tl_b))
                               else (tl_a or tl_b)[0]["source"]) if detected and (tl_a or tl_b) else None,
                    # per-team provenance so each side's badge is accurate
                    "sourceA": tl_a[0]["source"] if tl_a else None,
                    "sourceB": tl_b[0]["source"] if tl_b else None,
                    "confidenceA": info_a["confidence"] if info_a["comp"] else None,
                    "confidenceB": info_b["confidence"] if info_b["comp"] else None,
                    "detectedA": bool(info_a["comp"]),
                    "detectedB": bool(info_b["comp"]),
                },
                "notes": rv(mr, "notes"),
            })

        match_bans = hero_bans_for_map(con, m["id"])
        match_pick_veto = pick_veto_for_match(con, m["id"])
        matches_out.append({
            "id": m["id"],
            "date": m["date"],
            "status": m["status"],
            "region": m["region"],
            "stage": rv(m, "stage") or "",
            "eventName": rv(m, "event_name"),
            "season": rv(m, "season"),
            "division": rv(m, "division"),
            "round": rv(m, "round"),
            "groupName": rv(m, "group_name"),
            "scheduledAt": rv(m, "scheduled_at"),
            "startedAt": rv(m, "started_at"),
            "finishedAt": rv(m, "finished_at"),
            "teamA": m["team_a"],
            "teamB": m["team_b"],
            "teams": [m["team_a"], m["team_b"]],
            "scoreA": rv(m, "score_a", 0),
            "scoreB": rv(m, "score_b", 0),
            "score": {"a": rv(m, "score_a", 0), "b": rv(m, "score_b", 0)},
            "winnerTeam": rv(m, "winner_team"),
            # -------- FACEIT-sourced (matchroom facts) --------------------
            "faceit": {
                "matchId": rv(m, "faceit_match_id"),
                "roomUrl": rv(m, "faceit_room_url"),
                "rosters": rosters_for_match(con, m["id"], m["team_a"], m["team_b"]),
                "pickVeto": match_pick_veto,
                "heroBans": match_bans,
                "replayCodes": replay_codes,
            },
            "sourceUrl": rv(m, "source_url"),
            "vodUrl": rv(m, "vod_url"),
            "rawSource": rv(m, "raw_source"),
            "prepNotes": rv(m, "prep_notes"),
            "updatedAt": rv(m, "updated_at"),
            "maps": maps_out,
        })

    return {
        "generatedAt": dt.datetime.now(dt.timezone.utc).isoformat(),
        "heroes": heroes,
        "maps": maps,
        "teams": teams,
        "teamPrepNotes": prep_notes,
        "matches": matches_out,
        "videoSources": load_video_sources(),
        "autoRuns": load_auto_runs(),
        "ingestRuns": load_ingest_runs(con),
    }


