def _snapshot_times(con, map_result_id: int, team_id: str,
                    rounds: list) -> list[int]:
    """Round starts + every moment this team's comp changed."""
    times = {int(r["start_offset"]) for r in rounds
             if rv(r, "start_offset") is not None}
    for s in con.execute(
            """SELECT status, offset_seconds FROM hero_swaps
               WHERE map_result_id=? AND team_id=? AND status='confirmed'""",
            (map_result_id, team_id)):
        times.add(int(s["offset_seconds"]))
    return sorted(times)


def _comp_at(stints, team_id: str, t: int) -> tuple[list, list]:
    """(heroes[5], contributing stint rows) for one team at time t."""
    heroes, rows = [], []
    for slot in range(1, 6):
        cand = [s for s in stints
                if s["team_id"] == team_id and s["slot"] == slot
                and s["start_offset"] <= t + 15
                and (rv(s, "end_offset") or 10**9) >= t]
        if cand:
            # the stint that started most recently before t wins
            best = sorted(cand, key=lambda s: -s["start_offset"])[0]
            heroes.append(best["hero_id"])
            rows.append(best)
    return heroes, rows


def _nearest_frame(frames_dir_rel: str, t: int) -> str | None:
    """Closest copied evidence frame (reports/.../frames/frame_<t>.jpg)."""
    d = os.path.join(db.REPO_ROOT, frames_dir_rel)
    if not os.path.isdir(d):
        return None
    best, best_d = None, None
    for fn in os.listdir(d):
        if not fn.startswith("frame_"):
            continue
        try:
            ft = int(fn.split("_")[1].split(".")[0])
        except ValueError:
            continue
        dist = abs(ft - t)
        if best_d is None or dist < best_d:
            best_d, best = dist, fn
    if best is not None and best_d is not None and best_d <= 60:
        return f"{frames_dir_rel}/{best}"
    return None


def _layout_for_source(source_id: str | None) -> str | None:
    """Resolve a source id to its calibration layout file. Registered
    sources declare it in video_sources.json; ingest-only sources (e.g.
    owcs-jksix-qwc) follow the slug convention layouts/<id_>.json."""
    if not source_id:
        return None
    for s in load_video_sources():
        if s["id"] == source_id and s.get("layout"):
            p = os.path.join(db.REPO_ROOT, s["layout"])
            return p if os.path.exists(p) else None
    slug = source_id.replace("-", "_")
    p = os.path.join(db.REPO_ROOT, "layouts", f"{slug}.json")
    return p if os.path.exists(p) else None


def calibration_summary(source_id: str | None, roster: int) -> dict | None:
    """Compact, honest autocalibration provenance for a broadcast: the
    confidence the auto-calibrator earned, whether the gameplay-filter
    probe is present, and how much of the roster is templated. Powers the
    'why you can trust this footage' story on team/match pages. Returns
    None when the layout can't be found (never invents numbers)."""
    import re as _re
    path = _layout_for_source(source_id)
    if not path:
        return None
    try:
        with open(path, "r", encoding="utf-8") as f:
            layout = json.load(f)
    except (OSError, ValueError):
        return None
    cal = layout.get("calibration") or {}
    tdir = layout.get("templates_dir")
    heroes = 0
    if tdir and os.path.isdir(os.path.join(db.REPO_ROOT, tdir)):
        name_re = _re.compile(r"^([a-z0-9_-]+?)(?:\.(?:v\d+|[a-z]))?\.png$")
        seen = set()
        for fn in os.listdir(os.path.join(db.REPO_ROOT, tdir)):
            m = name_re.match(fn)
            if m:
                seen.add(m.group(1))
        heroes = len(seen)
    return {
        "sourceId": source_id,
        "confidence": cal.get("confidence"),
        "hudProbe": bool(layout.get("hud_probe")),
        "rejectMarkers": len(layout.get("reject") or []),
        "templateHeroes": heroes,
        "roster": roster,
        "frameSize": [layout.get("frame_width"), layout.get("frame_height")],
    }


def build_public_payload(con) -> dict:
    """The production public.v1 dataset, built ONLY from reviewed/staged
    DB state: matches, map_results, hero_stints (approved statuses),
    hero_swaps, ingest_runs. Nothing is invented; missing facts stay null
    with honest notes."""
    now = dt.datetime.now(dt.timezone.utc).isoformat()
    heroes = attach_portraits(
        [{"id": r["id"], "name": r["name"], "role": r["role"]}
         for r in con.execute("SELECT * FROM heroes ORDER BY role, name")])
    maps_catalog = [{"id": r["id"], "name": r["name"], "mode": r["mode"]}
                    for r in con.execute(
                        "SELECT * FROM game_maps ORDER BY name")]

    roster_size = con.execute("SELECT COUNT(*) FROM heroes").fetchone()[0]
    ingest_rows = con.execute(
        "SELECT * FROM ingest_runs WHERE status='complete'").fetchall()
    match_ids = sorted({r["match_id"] for r in ingest_rows
                        if r["match_id"]})
    hero_bans_out: list[dict] = []

    teams_needed: set[str] = set()
    matches_out, runs_out, snaps_out, players_out = [], [], [], []
    swaps_out: list[dict] = []
    vod_out: dict[str, dict] = {}
    tournaments_by_id: dict[str, dict] = {}

    for mid in match_ids:
        m = con.execute("SELECT * FROM matches WHERE id=?",
                        (mid,)).fetchone()
        if m is None:
            continue
        teams_needed.update((m["team_a"], m["team_b"]))
        tid = _tournament_id(m)
        maps_out = []
        for mr in con.execute(
                """SELECT * FROM map_results WHERE match_id=?
                   ORDER BY map_order""", (mid,)).fetchall():
            rounds = con.execute(
                """SELECT * FROM map_rounds WHERE map_result_id=?
                   ORDER BY round_index""", (mr["id"],)).fetchall()
            stints = _stints_for_map(con, mr["id"])
            run = next((r for r in ingest_rows
                        if r["match_id"] == mid
                        and r["map_order"] == mr["map_order"]), None)
            map_pub_id = f"{mid}-m{mr['map_order']}"
            mode = next((c["mode"] for c in maps_catalog
                         if c["id"] == mr["map_id"]), None)
            maps_out.append({
                "id": map_pub_id,
                "order": mr["map_order"],
                "map": mr["map_id"],
                "mode": mode,
                "winner": rv(mr, "winner_team"),
                "scoreA": rv(mr, "score_a"),
                "scoreB": rv(mr, "score_b"),
                # per-round capture % is not measured by the CV pipeline
                # yet -> null means the page renders its honest fallback.
                # roundCount IS real (segmented by the round-emblem detector)
                # — the honest first step toward per-submap detail.
                "scoreDetail": None,
                "roundCount": len(rounds),
                # real round windows from the round-emblem detector —
                # broadcast offsets in seconds, ±1 sample (~5 s).
                "rounds": [{"index": r_["round_index"],
                            "start": rv(r_, "start_offset"),
                            "end": rv(r_, "end_offset"),
                            "confidence": rv(r_, "confidence"),
                            "source": rv(r_, "source", "cv")}
                           for r_ in rounds],
                "pickedBy": rv(mr, "picked_by_team"),
                "pickNote": None,
            })
            # hero swaps for this map — the temporal-consensus verdicts,
            # confirmed AND rejected. Confirmed rows carry before/after
            # evidence crops; rejected rows carry the honest reason they
            # were thrown out. Nothing here is invented.
            for s in con.execute(
                    """SELECT * FROM hero_swaps WHERE map_result_id=?
                       ORDER BY offset_seconds""", (mr["id"],)):
                evb, eva = rv(s, "evidence_before"), rv(s, "evidence_after")
                swaps_out.append({
                    "id": f"sw-{map_pub_id}-{s['team_id']}-{s['slot']}-"
                          f"{s['offset_seconds']}-{s['status']}",
                    "matchId": mid,
                    "mapId": map_pub_id,
                    "teamId": s["team_id"],
                    "side": rv(s, "side"),
                    "slot": rv(s, "slot"),
                    "fromHero": s["from_hero"],
                    "toHero": s["to_hero"],
                    "offset": rv(s, "offset_seconds"),
                    "confidence": rv(s, "confidence"),
                    "status": s["status"],
                    "reason": rv(s, "reason"),
                    "evidenceBefore": evb if evb and os.path.exists(
                        os.path.join(db.REPO_ROOT, evb)) else None,
                    "evidenceAfter": eva if eva and os.path.exists(
                        os.path.join(db.REPO_ROOT, eva)) else None,
                    "ingestId": rv(s, "ingest_id"),
                })
            # hero bans for this map (real match facts; empty when the
            # broadcast/import recorded none — the UI says so honestly).
            for b in con.execute(
                    """SELECT * FROM hero_bans WHERE match_id=?
                       AND (map_result_id=? OR map_result_id IS NULL)
                       ORDER BY COALESCE(ban_order, id)""",
                    (mid, mr["id"])):
                hero_bans_out.append({
                    "id": f"ban-{map_pub_id}-{b['hero_id']}-{rv(b, 'team_id') or 'x'}",
                    "matchId": mid,
                    "mapId": map_pub_id,
                    "hero": b["hero_id"],
                    "teamId": rv(b, "team_id"),
                    "source": rv(b, "source", "manual"),
                })
            if run is None:
                continue
            frames_rel = f"reports/ingest/{run['id']}/frames"
            approved = {"auto-high", "reviewed"}
            for team in (m["team_a"], m["team_b"]):
                for t in _snapshot_times(con, mr["id"], team, rounds):
                    heroes5, rows = _comp_at(stints, team, t)
                    if len(heroes5) != 5:
                        continue
                    statuses = {r_["status"] for r_ in rows}
                    review = ("auto-high" if statuses <= approved
                              else "needs-review")
                    manual = any(rv(r_, "manual_override") for r_ in rows)
                    confs = [rv(r_, "mean_conf") for r_ in rows
                             if rv(r_, "mean_conf") is not None]
                    side = rows[0]["side"] if rows else None
                    snaps_out.append({
                        "id": f"cs-{map_pub_id}-{team}-{t}",
                        "matchId": mid,
                        "mapId": map_pub_id,
                        "teamId": team,
                        "side": side,
                        "timestamp": t,
                        "heroes": heroes5,
                        "source": "manual" if manual else "cv",
                        "confidence": (round(sum(confs) / len(confs), 3)
                                       if confs else None),
                        "reviewStatus": review,
                        "evidenceRunId": run["id"],
                        "evidenceFrame": _nearest_frame(frames_rel, t),
                    })

        run_ids = [r["id"] for r in ingest_rows if r["match_id"] == mid]
        for r in ingest_rows:
            if r["match_id"] != mid:
                continue
            frames_rel = f"reports/ingest/{r['id']}/frames"
            frames = []
            fdir = os.path.join(db.REPO_ROOT, frames_rel)
            if os.path.isdir(fdir):
                for fn in sorted(os.listdir(fdir)):
                    if fn.startswith("frame_"):
                        try:
                            off = int(fn.split("_")[1].split(".")[0])
                        except ValueError:
                            continue
                        frames.append({"offset": off,
                                       "file": f"{frames_rel}/{fn}",
                                       "layoutDebug": None})
            crops = []
            for s in con.execute(
                    """SELECT evidence_before, evidence_after
                       FROM hero_swaps WHERE ingest_id=?
                       AND status='confirmed'""", (r["id"],)):
                for c in (s["evidence_before"], s["evidence_after"]):
                    if c and os.path.exists(os.path.join(db.REPO_ROOT, c)):
                        crops.append(c)
            runs_out.append({
                "id": r["id"],
                "matchId": mid,
                "sourceId": rv(r, "source_id"),
                "window": {"start": _fmt_clock(rv(r, "start_offset")),
                           "end": _fmt_clock(rv(r, "end_offset")),
                           "every": 5},
                "requestedHeight": 720,
                "actualWidth": 1280,
                "actualHeight": 720,
                "clipMode": "direct-url-cut",
                "status": "needs-review",
                "reportPath": rv(r, "report_path"),
                "createdAt": rv(r, "created_at"),
                "frames": frames,
                "crops": crops,
                "note": ("full-map CV ingestion "
                         f"({rv(r, 'detector_version')}, calibration "
                         f"{rv(r, 'calibration_version')})"),
                # autocalibration provenance for the 'trust this footage'
                # story — real numbers from the source's layout, or null.
                "calibration": calibration_summary(
                    rv(r, "source_id"), roster_size),
                "calibrationStatus": rv(r, "calibration_status", "ok"),
                "calibrationVersion": rv(r, "calibration_version"),
            })

        for p in con.execute(
                """SELECT r.team_id, p.* FROM match_rosters r
                   JOIN players p ON p.id=r.player_id
                   WHERE r.match_id=?""", (mid,)):
            players_out.append({"id": p["id"], "teamId": p["team_id"],
                                "handle": p["nickname"],
                                "role": rv(p, "role")})

        vurl = rv(m, "vod_url")
        if vurl:
            vod_out[vurl] = {
                "id": rv(m, "source_ref") or f"vod-{mid}",
                "provider": "youtube",
                "url": vurl,
                "title": rv(m, "event_name") or "OWCS broadcast VOD",
                "matchIds": [mid],
                "heightAvailable": 1080,
            }

        stage_name = rv(m, "stage") or "Matches"
        matches_out.append({
            "id": mid,
            "tournamentId": tid,
            "stageId": f"{tid}-stage",
            "roundId": None,
            "teamA": m["team_a"],
            "teamB": m["team_b"],
            "bestOf": 5,
            "scheduledAt": f"{m['date']}T00:00:00+00:00",
            "status": "completed",
            "scoreA": rv(m, "score_a") or None,
            "scoreB": rv(m, "score_b") or None,
            "winner": rv(m, "winner_team"),
            "streamUrl": vurl,
            "faceitUrl": rv(m, "faceit_room_url"),
            "liquipediaUrl": None,
            "casters": [],
            "sources": [{"type": "vod", "url": vurl,
                         "lastSynced": now}] if vurl else [],
            "captureStatus": "needs-review",
            "captureRunId": run_ids[0] if run_ids else None,
            "summary": (f"{len(maps_out)} map(s) captured and ingested by "
                        "the CV pipeline; series score not yet recorded — "
                        "only verified data is shown."),
            "maps": maps_out,
        })

        if tid not in tournaments_by_id:
            tournaments_by_id[tid] = {
                "id": tid,
                "name": rv(m, "event_name") or "OWCS 2026",
                "series": "OWCS",
                "region": _region_id(rv(m, "region")),
                "tier": "S",
                "year": 2026,
                "startsAt": f"{m['date']}T00:00:00+00:00",
                "endsAt": None,
                "status": "completed",
                "prizePool": None,
                "teamIds": set(),
                "winnerTeamId": None,
                "summary": (f"{stage_name}. Matches appear here as their "
                            "VODs are captured and reviewed."),
                "logoUrl": None,
                "sources": [],
                "stages": [{"id": f"{tid}-stage", "name": stage_name,
                            "order": 1, "format": "Double elimination",
                            "status": "completed"}],
                "standings": [],
            }
        tourn = tournaments_by_id[tid]
        tourn["teamIds"].update((m["team_a"], m["team_b"]))
        if vurl and not any(s["url"] == vurl for s in tourn["sources"]):
            tourn["sources"].append({"type": "vod", "url": vurl,
                                     "lastSynced": now})

    tournaments_out = [dict(t, teamIds=sorted(t["teamIds"]))
                       for t in tournaments_by_id.values()]

    teams = [{"id": r["id"], "name": r["name"], "code": r["code"],
              "region": _region_id(rv(r, "region")),
              "logoUrl": rv(r, "logo_url")}
             for r in con.execute("SELECT * FROM teams ORDER BY name")
             if r["id"] in teams_needed]

    return {
        "meta": {
            "schema": "public.v1",
            "demo": False,
            "generatedAt": now,
            "note": ("Production export from data/owcs.sqlite — only "
                     "captured, staged and reviewed data; no invented "
                     "values."),
        },
        "regions": REGIONS,
        "teams": teams,
        "players": players_out,
        "tournaments": tournaments_out,
        "bracketRounds": [],
        "extraRounds": [],
        "bracketMatches": [],
        "matches": matches_out,
        "heroBans": hero_bans_out,
        "heroSwaps": swaps_out,
        "captureRuns": runs_out,
        "compSnapshots": snaps_out,
        "vodSources": list(vod_out.values()),
        "heroes": heroes,
        "mapsCatalog": maps_catalog,
        "patches": [],
    }


def write_public(con) -> str:
    payload = build_public_payload(con)
    ts = dt.datetime.now(dt.timezone.utc).strftime("%Y-%m-%d %H:%M")
    body = (PUBLIC_HEADER.format(ts=ts)
            + json.dumps(payload, indent=1, ensure_ascii=False) + ";\n")
    os.makedirs(os.path.dirname(PUBLIC_OUT_PATH), exist_ok=True)
    with open(PUBLIC_OUT_PATH, "w", encoding="utf-8") as f:
        f.write(body)
    print(f"Wrote {PUBLIC_OUT_PATH}: {len(payload['matches'])} matches, "
          f"{len(payload['compSnapshots'])} comp snapshots, "
          f"{len(payload['captureRuns'])} capture runs.")
    return PUBLIC_OUT_PATH


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--stdout", action="store_true",
                    help="print JSON instead of writing data.js")
    ap.add_argument("--public", action="store_true",
                    help="ALSO write assets/data/public_data.v1.js "
                         "(production public dataset)")
    args = ap.parse_args()

    con = db.connect()
    payload = build_payload(con)

    if args.stdout:
        json.dump(payload, sys.stdout, indent=1)
        return

    ts = dt.datetime.now(dt.timezone.utc).strftime("%Y-%m-%d %H:%M")
    body = HEADER.format(ts=ts) + json.dumps(payload, indent=1, ensure_ascii=False) + ";\n"
    os.makedirs(os.path.dirname(OUT_PATH), exist_ok=True)
    with open(OUT_PATH, "w", encoding="utf-8") as f:
        f.write(body)
    bans = sum(len(g["faceit"]["heroBans"]) for m_ in payload["matches"] for g in m_["maps"])
    detected = sum(1 for m_ in payload["matches"] for g in m_["maps"] if g["tracker"]["detected"])
    total = sum(len(m_["maps"]) for m_ in payload["matches"])
    print(f"Wrote {OUT_PATH}: {len(payload['matches'])} matches, "
          f"{total} maps ({detected} with tracker comps, "
          f"{total - detected} FACEIT-only), {bans} hero bans.")
    if args.public:
        write_public(con)


if __name__ == "__main__":
    main()
