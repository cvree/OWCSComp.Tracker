def rv(row: Any, key: str, default: Any = None) -> Any:
    """sqlite3.Row safe getter that also tolerates older DBs."""
    if row is None:
        return default
    try:
        return row[key]
    except Exception:
        return default


def snapshot_comp(con, snapshot_id: int) -> list[str]:
    return [r["hero_id"] for r in con.execute(
        "SELECT hero_id FROM snapshot_heroes WHERE snapshot_id=? ORDER BY slot",
        (snapshot_id,),
    ).fetchall()]


def comp_info_for_map(con, map_result_id: int, team_id: str) -> dict:
    """Union of heroes seen for this team on this map, ordered by the best
    snapshot's slots first. Opener is the earliest snapshot. Swap heroes are
    anything seen beyond the first five opener heroes."""
    snaps = con.execute(
        """SELECT id, overall_confidence, stream_offset_seconds,
                  COALESCE(source,'cv') AS source
           FROM comp_snapshots
           WHERE map_result_id=? AND team_id=?
           ORDER BY stream_offset_seconds ASC, id ASC""",
        (map_result_id, team_id),
    ).fetchall()
    # Manual corrections override every other source outright.
    manual = [s_ for s_ in snaps if s_["source"] == "manual"]
    if manual:
        snaps = manual
    if not snaps:
        return {"comp": [], "opener": [], "swaps": [], "confidence": None}

    opener = snapshot_comp(con, snaps[0]["id"])
    best = sorted(snaps, key=lambda s: (rv(s, "overall_confidence", 0) or 0), reverse=True)[0]
    comp: list[str] = []
    for hid in snapshot_comp(con, best["id"]):
        if hid not in comp:
            comp.append(hid)
    for s in snaps:
        for hid in snapshot_comp(con, s["id"]):
            if hid not in comp:
                comp.append(hid)
    confidence_values = [rv(s, "overall_confidence") for s in snaps if rv(s, "overall_confidence") is not None]
    confidence = round(sum(confidence_values) / len(confidence_values), 3) if confidence_values else None
    return {
        "comp": comp,
        "opener": opener,
        "swaps": [hid for hid in comp if hid not in opener[:5]],
        "confidence": confidence,
    }


def hero_bans_for_map(con, match_id: str, map_result_id: int | None = None) -> list[dict]:
    if map_result_id is None:
        rows = con.execute(
            """SELECT * FROM hero_bans WHERE match_id=?
               ORDER BY COALESCE(map_order, 999), COALESCE(ban_order, id), id""",
            (match_id,),
        ).fetchall()
    else:
        rows = con.execute(
            """SELECT * FROM hero_bans WHERE map_result_id=?
               ORDER BY COALESCE(ban_order, id), id""",
            (map_result_id,),
        ).fetchall()
    return [{
        "hero": r["hero_id"],
        "heroId": r["hero_id"],
        "role": rv(r, "role"),
        "teamId": rv(r, "team_id"),
        "mapOrder": rv(r, "map_order"),
        "order": rv(r, "ban_order"),
        "source": rv(r, "source", "unknown"),
        "confidence": rv(r, "confidence"),
        "notes": rv(r, "notes"),
    } for r in rows]


def pick_veto_for_match(con, match_id: str) -> list[dict]:
    rows = con.execute(
        """SELECT * FROM map_veto_events WHERE match_id=? ORDER BY order_index, id""",
        (match_id,),
    ).fetchall()
    return [{
        "order": r["order_index"],
        "teamId": rv(r, "team_id"),
        "map": rv(r, "map_id"),
        "action": rv(r, "action", "unknown"),
        "source": rv(r, "source", "unknown"),
        "notes": rv(r, "notes"),
    } for r in rows]


def rosters_for_match(con, match_id: str, team_a: str, team_b: str) -> dict:
    """FACEIT-sourced lineups: {'a': [players], 'b': [players]}."""
    out = {"a": [], "b": []}
    rows = con.execute(
        """SELECT r.team_id, p.id, p.nickname, p.role, p.faceit_player_id
           FROM match_rosters r JOIN players p ON p.id = r.player_id
           WHERE r.match_id=? ORDER BY p.role, p.nickname""", (match_id,))
    for r in rows:
        side = "a" if r["team_id"] == team_a else "b" if r["team_id"] == team_b else None
        if side:
            out[side].append({"id": r["id"], "nickname": r["nickname"],
                              "role": rv(r, "role"),
                              "faceitPlayerId": rv(r, "faceit_player_id")})
    return out


def comp_timeline(con, map_result_id: int, team_id: str) -> list[dict]:
    """Tracker-generated: every snapshot for this team on this map, in order."""
    rows = con.execute(
        """SELECT * FROM comp_snapshots WHERE map_result_id=? AND team_id=?
           ORDER BY stream_offset_seconds""", (map_result_id, team_id)).fetchall()
    manual = [r for r in rows if rv(r, "source", "cv") == "manual"]
    if manual:
        rows = manual
    out = []
    for s_ in rows:
        heroes = [r["hero_id"] for r in con.execute(
            "SELECT hero_id FROM snapshot_heroes WHERE snapshot_id=? ORDER BY slot",
            (s_["id"],))]
        out.append({"offsetSeconds": s_["stream_offset_seconds"],
                    "heroes": heroes,
                    "confidence": rv(s_, "overall_confidence"),
                    "source": rv(s_, "source", "cv")})
    return out


def winner_side(match_row, winner_team: str | None) -> str | None:
    if winner_team == match_row["team_a"]:
        return "a"
    if winner_team == match_row["team_b"]:
        return "b"
    return None


def load_auto_runs(path: str | None = None) -> list[dict]:
    """Recent run_owcs_auto.py run records for the static site."""
    path = path or os.path.join(db.REPO_ROOT, "data", "auto_runs.json")
