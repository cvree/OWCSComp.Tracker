def load_video_sources(path: str = SOURCES_PATH) -> list[dict]:
    """Metadata-only view of video_sources.json for the static site."""
    if not os.path.exists(path):
        return []
    try:
        with open(path, "r", encoding="utf-8") as f:
            entries = json.load(f).get("sources", []) or []
    except (ValueError, OSError):
        return []
    out = []
    for s in entries:
        sid = s.get("id") or s.get("match")
        if not sid:
            continue
        out.append({
            "id": sid,
            "title": s.get("title") or sid,
            "url": s.get("url") or s.get("vodUrl"),
            "platform": s.get("platform") or ("demo" if s.get("fixtureFrames") else "unknown"),
            "date": s.get("date"),
            "region": s.get("region"),
            "enabled": s.get("enabled", True),
            "layout": s.get("layout"),
            "mode": s.get("mode"),
            "notes": s.get("notes"),
        })
    return out

PORTRAIT_DIR = os.path.join(db.REPO_ROOT, "assets", "img", "heroes")


def hero_portraits() -> dict[str, str]:
    """{hero_id: site-relative url} for every hero with a REAL broadcast
    portrait (generated by pipeline/build_hero_portraits.py from harvested
    template crops). Heroes without one fall back to monogram tiles."""
    out: dict[str, str] = {}
    if os.path.isdir(PORTRAIT_DIR):
        for fn in sorted(os.listdir(PORTRAIT_DIR)):
            if fn.endswith(".png"):
                out[fn[:-4]] = f"assets/img/heroes/{fn}"
    return out


def attach_portraits(heroes: list[dict]) -> list[dict]:
    ports = hero_portraits()
    for h in heroes:
        if h["id"] in ports:
            h["portraitUrl"] = ports[h["id"]]
    return heroes


